function pickImages(images: string[]) {
  let selected: string[] = [];
  const length = images.length;

  // Pick three distinct random indexes
  while (selected.length < 3) {
    const index = Math.floor(Math.random() * length);
    if (!selected.includes(images[index])) {
      selected.push(images[index]);
    }
  }

  return selected;
}

